#!/usr/bin/env bash
set -euo pipefail

# ================================
# COLORS
# ===================================
RED='\e[0;31m'
GREEN='\e[0;32m'
YELLOW='\e[1;33m'
BLUE='\e[0;34m'
NC='\e[0m' # No Color

# ===================================
# GLOBAL CONFIGURATION
# ===================================
SILENT=false

# ===================================
# LOGGING
# ===================================
log() {
    if [ "$SILENT" != "true" ]; then
        echo -e "${BLUE}==> $1${NC}"
    fi
}
warn() {
    if [ "$SILENT" != "true" ]; then
        echo -e "${YELLOW}⚠️  $1${NC}" >&2
    fi
}
success() {
    if [ "$SILENT" != "true" ]; then
        echo -e "${GREEN}✓ $1${NC}"
    fi
}
abort() {
    if [ "$SILENT" != "true" ]; then
        echo -e "${RED}✗ $1${NC}" >&2
    fi
    exit 1
}

# ================================
# ADD USER TO SUDO GROUP
# ===================================
log "Adding current user ($USER) to sudo group..."
sudo usermod -aG sudo "$USER"

log "Verifying sudo group membership..."
if groups "$USER" | grep -qw "sudo"; then
    success "User '$USER' is now in the sudo group."
else
    abort "Failed to add user '$USER' to sudo group."
fi
